
/******************************************************/
/* includes */
/******************************************************/
#include "on_err.h"
#include <stdio.h>
#include <stdlib.h>

/******************************************************/
/* defines */
/******************************************************/
#define ERR_BUF_SIZE 4096

/******************************************************/
/* functions */
/******************************************************/
void on_err(int condition, char msg[ERR_BUF_SIZE])
{
	if(condition == 0)
		return;

	fprintf(stderr, msg, ERR_BUF_SIZE);
	exit(-1);
}
